
public class Calculadora {

	public int soma(int n1, int n2) {
		
		return n1 + n2;
		
	}
	
	public int subtracao(int n1, int n2) {
			
		return n1 - n2;
		
	}
	
	public int divisao(int n1, int n2) {
		
		return n1 / n2;
		
	}
	
	public int multiplicacao(int n1, int n2) {
		
		return n1 * n2;
		
	}

}
